package com.example.mifone_lib.model.other;

public class UpdateTokenFirebase {
    int code;
    String message;

    public UpdateTokenFirebase(int code, String message) {
        this.code = code;
        this.message = message;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
